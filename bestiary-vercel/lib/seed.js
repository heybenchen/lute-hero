export const hpAtLevel = (base, lvl) => Math.round(base * (1 + 0.4 * (lvl - 1)));

export function buildLevels(base, type) {
  const eff = TYPE_EFFECTS[type] || TYPE_EFFECTS.fire;
  return [1, 2, 3, 4, 5].map((lvl) => ({
    level: lvl,
    hp: hpAtLevel(base, lvl),
    effect: eff[lvl - 1] || "",
  }));
}

