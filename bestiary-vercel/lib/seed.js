export function seedMonsters() {
  return ROSTER.map(([name, type, baseHp, tagline], i) => ({
    id: `${slug(name)}-${i}`,
    name,
    type,
    baseHp,
    tagline,
    imageUrl: "",
    hidden: false,
    levels: buildLevels(baseHp, type),
  }));
}
